let bestScore = Number(localStorage.getItem("tresletras_best") || "0");
let roundCount = 0;

const usedWords = new Set();

bestScoreEl.textContent = String(bestScore);
totalScoreEl.textContent = String(totalScore);

openRules(); // mostrar reglas al inicio

// ===================== Helpers =====================
function pad2(n){ return String(n).padStart(2,"0"); }

function setMessage(text, type=""){
  message.className = "msg " + (type || "");
  message.textContent = text;
}

function normalizeWord(str){
  // 1) trim, uppercase
  // 2) quitar tildes (áéíóúü) -> aeiouu, mantener Ñ
  // 3) quitar caracteres no letras (pero NO aceptamos espacios/guiones como válidos)
  const up = (str || "").trim().toUpperCase();

  // Normalizamos tildes sin romper Ñ:
  // - Convertimos a NFD y quitamos diacríticos EXCEPTO la Ñ.
  // Truco: temporalmente reemplazamos Ñ por un marcador.
  const mark = up.replaceAll("Ñ", "__ENIE__");
  const noDiacritics = mark
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "") // quita tildes
    .replaceAll("__ENIE__", "Ñ");

  return noDiacritics;
}

function hasForbiddenSeparators(raw){
  // Prohibimos palabras compuestas: espacios o guiones
  return /[\s-]/.test(raw);
}

function containsInOrder(word, a, b, c){
  // word ya normalizada
  const i1 = word.indexOf(a);
  if(i1 === -1) return false;
  const i2 = word.indexOf(b, i1 + 1);
  if(i2 === -1) return false;
  const i3 = word.indexOf(c, i2 + 1);
  return i3 !== -1;
}

function letterPoints(ch){
  // Ñ, W, X, Y, Z = 4; resto = 2
  if(["Ñ","W","X","Y","Z"].includes(ch)) return 4;
  // A-Z estándar = 2
  if(/^[A-ZÑ]$/.test(ch)) return 2;
  return 0;
}

function scoreWord(wordNorm){
  let sum = 0;
  let specials = 0;

  for(const ch of wordNorm){
    const pts = letterPoints(ch);
    sum += pts;
    if(["Ñ","W","X","Y","Z"].includes(ch)) specials++;
  }

  // Bonos opcionales
  if(wordNorm.length >= 16) sum += 10;
  else if(wordNorm.length >= 14) sum += 5;

  if(specials >= 3) sum += 15;

  return sum;
}

function updateChips(lastWordScore=0){
  chipLetters.textContent = `Letras: ${baseLetters.join(" - ")}`;
  chipRound.textContent = `Ronda: ${roundCount}`;
  chipWordScore.textContent = `+${lastWordScore}`;
}

// ===================== Slot animation =====================
function setSlotChar(slotEl, ch){
  slotEl.querySelector(".slotChar").textContent = ch;
}

function randomLetter(){
  return LETTERS[Math.floor(Math.random()*LETTERS.length)];
}

async function spinSlots(durationMs=900){
  if(spinning) return;
  spinning = true;

  const start = Date.now();
  let a="-", b="-", c="-";

  // “Efecto tragamonedas”: cambia letras rápido
  while(Date.now() - start < durationMs){
    a = randomLetter();
    b = randomLetter();
    c = randomLetter();
    setSlotChar(slot1, a);
    setSlotChar(slot2, b);
    setSlotChar(slot3, c);
    await sleep(60);
  }

  // Resultado final
  baseLetters = [randomLetter(), randomLetter(), randomLetter()];
  setSlotChar(slot1, baseLetters[0]);
  setSlotChar(slot2, baseLetters[1]);
  setSlotChar(slot3, baseLetters[2]);

  spinning = false;
  updateChips(0);
}

function sleep(ms){ return new Promise(r => setTimeout(r, ms)); }

// ===================== Timer =====================
function renderTimer(){
  timerEl.textContent = `00:${pad2(timeLeft)}`;
}

function startTimer(){
  clearInterval(timerId);
  timerId = setInterval(() => {
    timeLeft--;
    renderTimer();

    if(timeLeft <= 0){
      endGame();
    }
  }, 1000);
}

function endGame(){
  clearInterval(timerId);
  timerId = null;

  wordInput.disabled = true;
  btnSend.disabled = true;
  btnStart.disabled = false;

  setMessage(`⏱️ Tiempo terminado. Puntaje total: ${totalScore}`, "warn");

  if(totalScore > bestScore){
    bestScore = totalScore;
    localStorage.setItem("tresletras_best", String(bestScore));
    bestScoreEl.textContent = String(bestScore);
    setMessage(`🏆 Nuevo mejor puntaje: ${bestScore}`, "ok");
  }
}

// ===================== Game flow =====================
function resetGame(){
  clearInterval(timerId);
  timerId = null;

  baseLetters = ["-","-","-"];
  setSlotChar(slot1, "-");
  setSlotChar(slot2, "-");
  setSlotChar(slot3, "-");

  timeLeft = 30;
  renderTimer();

  totalScore = 0;
  totalScoreEl.textContent = "0";
  roundCount = 0;

  usedWords.clear();
  wordsList.innerHTML = "";

  wordInput.value = "";
  wordInput.disabled = true;
  btnSend.disabled = true;

  btnStart.disabled = false;

  updateChips(0);
  setMessage("Listo. Presiona Empezar.", "");
}

async function startGame(){
  btnStart.disabled = true;
  setMessage("🎰 Generando letras...", "");

  // nueva partida
  totalScore = 0;
  totalScoreEl.textContent = "0";
  roundCount = 1;
  usedWords.clear();
  wordsList.innerHTML = "";
  wordInput.value = "";
  updateChips(0);

  timeLeft = 30;
  renderTimer();

  await spinSlots(1000);

  // habilitar juego
  wordInput.disabled = false;
  btnSend.disabled = false;
  wordInput.focus();

  setMessage("✅ ¡Ya! Escribe palabras y presiona Enviar.", "ok");
  startTimer();
}

function addWordToList(wordRaw, pts){
  const li = document.createElement("li");
  li.textContent = `${wordRaw.toUpperCase()}  (+${pts})`;
  wordsList.prepend(li);
}

function validateAndScore(){
  const raw = wordInput.value.trim();
  if(!raw){
    setMessage("Escribe una palabra.", "bad");
    return;
  }

  if(hasForbiddenSeparators(raw)){
    setMessage("No se permiten palabras compuestas (sin espacios ni guiones).", "bad");
    return;
  }

  const norm = normalizeWord(raw);

  // Solo letras A-Z y Ñ (después de normalizar)
  if(!/^[A-ZÑ]+$/.test(norm)){
    setMessage("Solo se permiten letras (A-Z y Ñ).", "bad");
    return;
  }

  // Validación de orden
  const [A,B,C] = baseLetters;
  if(!containsInOrder(norm, A, B, C)){
    setMessage(`Debe contener ${A}-${B}-${C} en ese orden.`, "bad");
    return;
  }

  // No repetir palabra en la misma partida
  if(usedWords.has(norm)){
    setMessage("Esa palabra ya la usaste en esta partida.", "warn");
    return;
  }

  // Validación de existencia (diccionario local)
  if(!DICT.has(norm)){
    setMessage("No está en el diccionario local (MVP).", "bad");
    return;
  }

  // Puntaje
  const pts = scoreWord(norm);
  totalScore += pts;
  totalScoreEl.textContent = String(totalScore);

  usedWords.add(norm);
  addWordToList(raw, pts);

  updateChips(pts);
  setMessage(`✅ Válida: +${pts} puntos`, "ok");

  wordInput.value = "";
  wordInput.focus();
}

// ===================== Modal Rules =====================
function openRules(){
  rulesModal.classList.add("show");
  rulesModal.setAttribute("aria-hidden", "false");
}
function closeRules(){
  rulesModal.classList.remove("show");
  rulesModal.setAttribute("aria-hidden", "true");
}

// ===================== Events =====================
btnStart.addEventListener("click", startGame);
btnReset.addEventListener("click", resetGame);

btnSend.addEventListener("click", () => {
  if(timerId) validateAndScore();
});

wordInput.addEventListener("keydown", (e) => {
  if(e.key === "Enter"){
    e.preventDefault();
    if(timerId) validateAndScore();
  }
});

btnRules.addEventListener("click", openRules);
btnCloseRules.addEventListener("click", closeRules);
btnOkRules.addEventListener("click", closeRules);
rulesModal.addEventListener("click", (e) => {
  if(e.target === rulesModal) closeRules();
});

